#define MMIO8(Addr) ((volatile uint8_t*)(Addr))

#define UART0_BASE 0xf0000010
#define UART0_RHR MMIO8(UART0_BASE + 0x00)
#define UART0_THR MMIO8(UART0_BASE + 0x00)
#define UART0_IER MMIO8(UART0_BASE + 0x04)
#define UART0_ISR MMIO8(UART0_BASE + 0x08)
#define UART0_FCR MMIO8(UART0_BASE + 0x08)
#define UART0_LCR MMIO8(UART0_BASE + 0x0c)
#define UART0_MCR MMIO8(UART0_BASE + 0x10)
#define UART0_LSR MMIO8(UART0_BASE + 0x14)
#define UART0_MSR MMIO8(UART0_BASE + 0x18)
#define UART0_SPR MMIO8(UART0_BASE + 0x1c)
